    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        if(textbookid.getText().equals("")){
            JOptionPane.showMessageDialog(this," Please enter book id and search it");
            textbookid.requestFocus();
        }
        
        else{
            try {
                pst=con.prepareStatement("Select * from book where id=?");
                pst.setString(1, textbookid.getText());
                
                rs=pst.executeQuery();
                if(rs.next()){
                    textbookname.setText(rs.getString(2));
                }
                
            } catch (SQLException ex) {
                Logger.getLogger(IssueBook.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

// TODO add your handling code here:
    }//GEN-LAST:event_btnsearchActionPerformed

    private void btnissueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnissueActionPerformed
      if(textbookid.getText().equals("")){
            JOptionPane.showMessageDialog(this," Please enter book id and search it");
            textbookid.requestFocus();
        }
      else{
          
          try {
              pst=con.prepareStatement("Update book set status=?, issue=?,due=?,studentid=?");
             
              pst.setString(1, "ISSUED");
              pst.setString(2, textissuedate.getText());
              pst.setString(3, textduedate.getText());
              pst.setString(4, textstudentid.getText());
              pst.executeUpdate();
              JOptionPane.showMessageDialog(this, "Book issued");
              textbookid.setText("");
              textbookname.setText("");
              textstudentid.setText("");
              textissuedate.setText("");
              textduedate.setText("");
              
              
              
          } catch (SQLException ex) {
              Logger.getLogger(IssueBook.class.getName()).log(Level.SEVERE, null, ex);
          }
          
      }
        
// TODO add your handling code here:
    }//GEN-LAST:event_btnissueActionPerformed

